        public class TradeItemDef
        {
            public string   PrefabName  { get; }
            public int      Stack       { get; }
            public int      Price       { get; }
            public string   RequiredKey { get; }
            public Category Cat         { get; }

            public TradeItemDef(string prefabName, int stack, int price, string requiredKey = null, Category cat = Category.Misc)
            {
                PrefabName  = prefabName;
                Stack       = stack;
                Price       = price;
                RequiredKey = requiredKey;
                Cat         = cat;
            }
        }

        /// <summary>
        /// Retourne uniquement les items dont la catégorie est activée dans la config.
        /// </summary>
        public static IEnumerable<TradeItemDef> GetEnabled()
        {
            foreach (var def in GetAll())
            {
                if (!IsCategoryEnabled(def.Cat)) continue;
                yield return def;
            }
        }

        private static bool IsCategoryEnabled(Category cat) => cat switch
        {
            Category.Materials   => ModConfig.EnableMaterials.Value,
            Category.Food        => ModConfig.EnableFood.Value,
            Category.Weapons     => ModConfig.EnableWeapons.Value,
            Category.Armor       => ModConfig.EnableArmor.Value,
            Category.Ammo        => ModConfig.EnableAmmo.Value,
            Category.Consumables => ModConfig.EnableConsumables.Value,
            Category.Misc        => ModConfig.EnableMisc.Value,
            _                    => true,
        };

